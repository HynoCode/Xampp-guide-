    <div class="content">

        <p>&nbsp;&nbsp;El último paso es colocar la ruta de XAMPP dentro de las variables de entorno del ordenador. Para ello se debe acceder al panel de <i>sistema</i> del ordenador.</p>

        <img src="Resources/VARENT1.jpg" alt="imagen_escritorio" class="images">

        <p>&nbsp;&nbsp;Acceder a <i>Configuración avanzada del sistema</i>, ubicada en la parte superior derecha.</p>

        <img src="Resources/VARENT2.jpg" alt="imagen_escritorio" class="images">

        <p>&nbsp;&nbsp;Se mostrará una ventana con opciones del sistema y las variables de entorno la cual hay que seleccionar.</p>

        <img src="Resources/VARENT3.jpg" alt="imagen_escritorio" class="images">

        <p>&nbsp;&nbsp;Y se abrirá el panel para configurar las variables de usuario, se selecciona el <i>Path</i> y luego la opción editar.</p>

        <img src="Resources/VARENT4.jpg" alt="imagen_escritorio" class="images">

        <p>&nbsp;&nbsp;Por ultimo solo queda seleccionar <i>Nuevo</i> y pegar el Path o ruta donde se encuentra instalada la carpeta raiz de XAMPP. Aceptar todos los cambios y reiniciar el ordenador.</p>

